package main

import "fmt"

type specialError struct {
	message string
}

func (err specialError) Error() string {
	return fmt.Sprintf("[ERROR] Message: %v", err.message)
}

func specialErrorParam(err error) {
	fmt.Println("Arguments received: ", err.(specialError).message, "\n Complete error:", err)
}

func main() {
	arg := specialError{"EMOJIS!!!!!!!!"}
	specialErrorParam(arg)
}
