package main

import (
	"encoding/json"
	"fmt"
)

type user []struct {
	First   string   `json:"First"`
	Last    string   `json:"Last"`
	Age     int      `json:"Age"`
	Sayings []string `json:"Sayings"`
}

func main() {
	stringJSON := `[{"First":"James","Last":"Bond","Age":32,"Sayings":["Shaken, not stirred","Youth is no guarantee of innovation","In his majesty's royal service"]},{"First":"Miss","Last":"Moneypenny","Age":27,"Sayings":["James, it is soo good to see you","Would you like me to take care of that for you, James?","I would really prefer to be a secret agent myself."]},{"First":"M","Last":"Hmmmm","Age":54,"Sayings":["Oh, James. You didn't.","Dear God, what has James done now?","Can someone please tell me where James Bond is?"]}]`
	fmt.Println("In JSON: ", stringJSON)

	fmt.Println("")

	var result user
	err := json.Unmarshal([]byte(stringJSON), &result)
	if err != nil {
		fmt.Println("[ERROR]: ", err)
	}

	fmt.Println("In slice of structs: ", result)
	fmt.Println("")
	fmt.Println("Show one struct: ", result[1])
	fmt.Println("")
	fmt.Println("Show one item of one struct: ", result[1].Last)
}
