package main

import (
	"fmt"
)

func sendChannel(channel chan<- int, number int) {
	channel <- number
}

func receiveChannel(channel <-chan int) {
	fmt.Println("Value received of channel: ", <-channel)
}

func main() {
	myNumber := 120
	myChannel := make(chan int)

	go sendChannel(myChannel, myNumber)
	receiveChannel(myChannel)
}
